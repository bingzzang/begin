package codinglife.c17Array;

public class LenthDemo {

	public static void main(String[] args) {
		
		String[] language = new String [4];
		
		language[0] = "JAVA";
		System.out.println(language.length);
		
		language[1] = "JS";
		System.out.println(language.length);
		
		language[2] = "CSS";
		System.out.println(language.length);
		
		language[3] = "HTML";
		System.out.println(language.length);
	}
}
