package lecture.java.sec02;

public class Data01 {
	 
	public static void main(String[] args) {
		char c = 'a';
		System.out.println("c : "+c);
		
		int i = 10;
		System.out.println("i : "+i);
		
		double d = 10.232;
		System.out.println("d : "+d);
		
		boolean b = true;
		System.out.println("b : "+b);
		//위 : 기본형
		
		//아래: 참조형
		String s = "Hello World";
		System.out.println("s : "+s);
	}

}
