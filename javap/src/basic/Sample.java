package basic;

class Sample{
    public static void main(String[] args){
        int num;		//변수 선언
        num = 3; 		//변수의 값 대입
        
        System.out.println("변수 num 값은 "+num+"입니다");  //변수의 값 출력
        
        num = 5;		//새 값을 변수에 대입
        
        System.out.println("새로운 변수 num 값은 "+num+"입니다"); //새로운 변수값 출력
    }
}
